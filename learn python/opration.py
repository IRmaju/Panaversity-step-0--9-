# Lesson 03: Operators, Keywords & Variables

# Variables
x = 10  # Integer variable
y = 5.5  # Float variable
name = "Python"  # String variable
is_coding_fun = True  # Boolean variable

# Operators
sum_result = x + y  # Addition
sub_result = x - y  # Subtraction
mul_result = x * y  # Multiplication
div_result = x / y  # Division
mod_result = x % 3  # Modulus (remainder)
exp_result = x ** 2  # Exponentiation (power)

# Comparison Operators
is_equal = (x == y)  # Equal to
is_not_equal = (x != y)  # Not equal to
is_greater = (x > y)  # Greater than
is_less = (x < y)  # Less than

# Logical Operators
and_result = (x > 5 and y < 10)  # Both conditions must be True
or_result = (x > 15 or y < 10)  # At least one condition must be True
not_result = not(x > 5)  # Reverses the boolean value

# Keywords (Python ke reserved words jo variables banane mein use nahi kar sakte)
# Example: False, None, True, and, as, assert, break, class, continue, def, etc.

# Displaying the results
print("Addition:", sum_result)
print("Subtraction:", sub_result)
print("Multiplication:", mul_result)
print("Division:", div_result)
print("Modulus:", mod_result)
print("Exponentiation:", exp_result)
print("Is Equal:", is_equal)
print("Is Not Equal:", is_not_equal)
print("Is Greater:", is_greater)
print("Is Less:", is_less)
print("AND Result:", and_result)
print("OR Result:", or_result)
print("NOT Result:", not_result)

# Variables Naming Rules
# Valid: my_var, myVar123, _myVar
# Invalid: 123var, my-var, my var
